     private void populateSelectedFlights(ArrayList<Flights> searchedFlights){
        this.searchedFlights = searchedFlights;
        DefaultTableModel dtm = (DefaultTableModel)flightScheduleTbl.getModel();
        dtm.setRowCount(0);
        for(Flights f : searchedFlights){
            Object[] row = new Object[dtm.getColumnCount()];
            row[0]=f;
            row[1]=f.getFlightNo();
            row[2]=f.getDeparture();
            row[3]=f.getArrival();
            SimpleDateFormat formatter = new SimpleDateFormat("dd-MM-yyyy");
            String date = formatter.format(f.getDate());
            row[4]=date;
            dtm.addRow(row);
            }       
 }
     

