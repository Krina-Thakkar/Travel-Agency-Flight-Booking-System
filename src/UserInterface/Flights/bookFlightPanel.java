    private void selectFlightsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectFlightsActionPerformed
        // TODO add your handling code here:
        int selectRow = flightScheduleTbl.getSelectedRow();
        if(selectRow<0){
            JOptionPane.showMessageDialog(null, "Please select a flight");
        }else{
            Flights flight = (Flights) flightScheduleTbl.getValueAt(selectRow, 0);
          //  System.out.println(flight);
            selectedFLightsPanel Panel = new selectedFLightsPanel(leftpanel,custdir,flight);
            leftpanel.add("selectedFlightsPanel",Panel);
            CardLayout layout = (CardLayout) leftpanel.getLayout();
            layout.next(leftpanel);
        }
    }//GEN-LAST:event_selectFlightsActionPerformed

