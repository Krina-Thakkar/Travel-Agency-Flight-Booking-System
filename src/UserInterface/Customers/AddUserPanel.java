    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        Customers customer = custdir.addCustomer();
        if(customerIDTextField.getText().equals("") || nameTextField.getText().equals("") || emailTextField.getText().equals("") || phoneTextField.getText().equals(""))
            {
                JOptionPane.showMessageDialog(null, "Field is Mandatory");
            }
        Pattern pattern = Pattern.compile("[a-z]*$");
        Matcher m = pattern.matcher(nameTextField.getText());
        Pattern p = Pattern.compile("[a-z]*$");
        Matcher ma = p.matcher(customerIDTextField.getText());
        Pattern pat = Pattern.compile("[0-9]{10}$");
        Matcher mat = pat.matcher(phoneTextField.getText());
        if(!(m.matches()) || !(ma.matches()) || !(mat.matches()))
        {
            JOptionPane.showMessageDialog(null, "Invalid Content!");
        }
        else{
        customer.setCustomerid(customerIDTextField.getText());
        customer.setName(nameTextField.getText());
        customer.setEmail(emailTextField.getText());
        customer.setPhone(phoneTextField.getText());
        
        JOptionPane.showMessageDialog(null,"Customer Added Successfully!!");
        populate();
                }
    }//GEN-LAST:event_saveBtnActionPerformed

