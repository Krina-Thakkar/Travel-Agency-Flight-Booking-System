     private void populate(){
        DefaultTableModel dtm = (DefaultTableModel)tblDirectory.getModel();
        dtm.setRowCount(0);
        for(Customers c : custdir.getCustomerlist()){
            Object[] row = new Object[dtm.getColumnCount()];
            row[0]=c;
            row[1]=c.getName();
            row[2]=c.getEmail();
            row[3]=c.getPhone();
            dtm.addRow(row);
        }
    }
    
